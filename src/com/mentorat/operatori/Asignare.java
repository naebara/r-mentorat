package com.mentorat.operatori;

public class Asignare {
    public static void main(String[] args) {
        // operatori -> simboluri care mereu fac o singura chestie
        // aritmetici , asignare ( modificare )

        int a = 50;
        // creste valoarea cu 3
        // modific valuarea lui a, adaugandu-i 3


        // 1 - prima varianta
        a = a + 3;
        System.out.println(a); // 13
        // noua valoare a lui a este egala cu
        // vechea valoare + 3

        a = a - 4;
        a = a * 6;

        // 2 - a doua varianta
        int b = 10;
        b += 3;
        b -= 3;
        b *= 3;
        b /= 3;
        b %= 3;

        System.out.println("b : " + b);




        // 1. ai un numar
        // Afiseaza dublul numarui
        // Modifica valorile cu operatorii artmetici

        int x = 3;
        // 3 6 2 5 1 0

    }
}
